package ALLPROGRAM;

public class Assignmentoperaters {
	
	public static void main(String args[]) {
		
		
	
		int num = 10;
		String name = "java ";
		
		System.out.println("num " + num);
		System.out.println("name " + name );
		
		int a = 10;
		a+=10;
		System.out.println("value of a is " + a);
		
		
		//&& (and) operator > - greater
		System.out.println((5>3) && (8>6));
		
		
	
		// || (or) operator
		System.out.println((8<3) || (5>3));
		
		
		
		//! NOT operaters 
		System.out.println("! - not operaters");
		System.out.println(!(5<3));
		
		
		
		
				
		
				
	}

	
	
}
